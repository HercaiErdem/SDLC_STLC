public class SDLCNedir {

    // SDLC Nedir?

    //SDLC'nin açılımı: Softaware Development Life Cycle (Yazilim Gelistirme Yasam Dongusu)
    /*
    SDLC mümkün olan en kısa sürede, en yüksek kalitede((High-Quality)), en düşük maliyetle
    ve Kullanici Taleplerini (User Expectation) karşılayan yazılım üreten bir süreçtir(Process).
    */

    // SDLC’nin Faydaları:
    /*
    1)Projenin takibini ve kontrolunu saglar,
    5)Projenin sürekli takibi ve kontrolu sayesinde, projede oluşabilecek bug ların risklerini azaltir,
    3)Yapilan planlama ve toplantilarla Projenin Olusturulma ve gelistirme (Development) hizini
    artirir,
    4)Oluşturulan detaylı planlama dokumanları sayesinde,Çalışanlar neyi, neden yapmaları gerektiğini
    anlarlar,
    5)Tüm ekibin iletişimini güçlendirir.
    */

    //SDLC altı aşamadan oluşur:
    /*
    1) Planning and Requirement Analysis (Planlama ve İhtiyaç Analizi):
    *SDLC’ nin bu aşamasında, müşteriden gelen fikirler de göz önünde bulundurularak ekibin kıdemli üyeleri
    (expert) tarafından gerçekleştirilir.
    *Proje de oluşabilecek ilgili riskleri detaylandırır ve bu riskleri azaltmak için alt planlar sunulur.

    2) Defining Requirements (Gereksinimleri Tanımlama):
    *Bu aşamada, ürün gereksinimlerini açıkça tanımlamak ve belgelendirmektir (dokumante etmek) müşteriden
    (Stakeholder) onay alınır.
    *Bu proje yaşam döngüsü boyunca tasarlanacak ve geliştirilecek tüm ürün
    gereksinimlerini içeren BRD ve FRD Teknik Islev Ihtiyaclari Dokumani hazirlanir.

    **BRD(Business Requirement Document): İş Gereksinimleri Dokümanı iş ihtiyaçlarını ve paydaş gereksinimleri
    açıklayan bir gereklilik paketidir.

    **FRD(Functional Requirement Document):  Doğrudan yazılımda kullanılacak doküman

    3) Designing the product architecture (Ürün dizaynını tasarlama):
    *BRD'de belirtilen gereksinimlere dayanarak, ürün mimarisi için genellikle birden fazla tasarım yaklaşımı
    taslağı olusturulur.

    **DDS(Design Document Specification): Tasarım spesifikasyonu, bir ürün veya süreçle ilgili noktaların bir
    listesini sağlayan ayrıntılı bir belgedir.

    4) Building or Developing the Product (Ürünü oluşturma veya geliştirme):
    *Bu aşamasında gerçek gelişme başlar ve ürün inşa edilir.
    *geliştirme ekibi, yol haritasında iletilen (Kodlama için FRD baz alinarak) üst düzey genel bakışı bir
    dizi taktiksel görev, bitiş tarihi ve günlük çalışma programlarına dönüştürür.

    5) Testing the Product (Ürünü test etme):
    *Bu aşama, ürün BRD'de tanımlanan kalite standartlarına ulaşıncaya kadar, ürün kusurlarının (bug) rapor
    edildiği, izlendiği, duzeltigi ve tekrar test edildiği aşamadir.

    6) Deployment in the Market and Maintenance (Ürünü pazarlama ve bakım):
    *Bu aşama, ürün test edildikten ve onaylandıktan sonra  müşterilerin satın alması ve kullanması için
    release(piyasaya sürülür) edildiği aşamadır.
    *Ürün piyasaya sunulduktan sonra mevcut müşteri tabanı için bakımı yapılır.
    *Musteriden gelen feedbackler ve Teknolojik Gelismeler ile ihtiyaclar yeniden belirlenir
    ve dongu yeniden baslatilir.

    */

    // SDLC TEAM

      /*
      1) Project Manager (PM)=> Proje Yöneticisi
      2) Business Analyst (BA)=> İş Analisti
      3) Developer (Dev)=> Yazılımcı
     4) Quality Analyst (QA)=> Kalite Analisti (Tester)
    oluşturmaktadır.
     */


}
